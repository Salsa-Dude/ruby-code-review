
first_name = "Joseph "
last_name = "Arias"

puts first_name + last_name
puts first_name += last_name

# concat - it mutates the original string
puts first_name.concat(" Alexis")

# shovel method
puts first_name << " lino"

# prepend - puts in front of the string
# also overwrites the original string
puts first_name.prepend("King ")
